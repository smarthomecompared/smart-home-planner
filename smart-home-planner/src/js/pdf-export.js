// ─── Utilities ─────────────────────────────────────────────────────────────────

function _groupBy(arr, keyFn) {
    return (arr || []).reduce((acc, item) => {
        const key = String(keyFn(item) || '');
        if (!acc[key]) acc[key] = [];
        acc[key].push(item);
        return acc;
    }, {});
}

function _fmtConnectivity(value) {
    if (!value) return '-';
    return typeof formatConnectivity === 'function' ? formatConnectivity(value) : String(value);
}

function _fmtType(value) {
    if (!value) return '-';
    return typeof formatDeviceType === 'function' ? formatDeviceType(value) : String(value);
}

function _fmtPower(value) {
    const v = String(value || '').toLowerCase();
    if (v === 'wired')   return 'Wired';
    if (v === 'battery') return 'Battery';
    return value || '-';
}

function _blobToDataUrl(blob) {
    return new Promise((resolve, reject) => {
        const reader   = new FileReader();
        reader.onload  = () => resolve(reader.result);
        reader.onerror = () => reject(new Error('FileReader failed'));
        reader.readAsDataURL(blob);
    });
}

/** Fetches a URL and returns a base64 data URL, or throws on failure. */
async function _fetchDataUrl(url) {
    const resp = await fetch(url);
    if (!resp.ok) throw new Error(`HTTP ${resp.status}`);
    return _blobToDataUrl(await resp.blob());
}

function _imgNaturalSize(dataUrl) {
    return new Promise((resolve, reject) => {
        const img   = new Image();
        img.onload  = () => resolve({ width: img.naturalWidth, height: img.naturalHeight });
        img.onerror = () => reject(new Error('Image load failed'));
        img.src     = dataUrl;
    });
}

// ─── Pie chart helpers ─────────────────────────────────────────────────────────

/**
 * Draws a filled polygon approximating a pie sector.
 * Angles in radians; 0 = right, −π/2 = top (same as Math.cos/sin convention).
 */
function _drawPieSlice(doc, cx, cy, r, startAngle, endAngle, color) {
    const span = endAngle - startAngle;
    if (Math.abs(span) < 0.001) return;
    const steps = Math.max(6, Math.ceil(Math.abs(span) * 10));

    // Build relative-coordinate segments for doc.lines():
    // Start from center → arc start → arc steps → back to center
    const segs = [];
    const ax0  = cx + Math.cos(startAngle) * r;
    const ay0  = cy + Math.sin(startAngle) * r;
    segs.push([ax0 - cx, ay0 - cy]); // center → arc start

    for (let i = 1; i <= steps; i++) {
        const a   = startAngle + span * i / steps;
        const pa  = startAngle + span * (i - 1) / steps;
        segs.push([
            (cx + Math.cos(a) * r) - (cx + Math.cos(pa) * r),
            (cy + Math.sin(a) * r) - (cy + Math.sin(pa) * r),
        ]);
    }

    const axN = cx + Math.cos(endAngle) * r;
    const ayN = cy + Math.sin(endAngle) * r;
    segs.push([cx - axN, cy - ayN]); // arc end → center

    doc.setFillColor(...color);
    doc.lines(segs, cx, cy, [1, 1], 'F', true);
}

/**
 * Draws a donut-style pie chart.
 * slices = [{ label, count, color }]
 * total  = denominator (sum of all visible counts, may differ from devices.length)
 */
function _drawPieChart(doc, cx, cy, r, slices, total) {
    if (!slices || !slices.length || !total) return;

    let startAngle = -Math.PI / 2; // start from top
    slices.forEach(slice => {
        if (slice.count <= 0) return;
        const angle = (slice.count / total) * 2 * Math.PI;
        _drawPieSlice(doc, cx, cy, r, startAngle, startAngle + angle, slice.color);
        startAngle += angle;
    });

    // Donut hole (erase centre)
    doc.setFillColor(..._C.cardBg);
    doc.circle(cx, cy, r * 0.44, 'F');
}


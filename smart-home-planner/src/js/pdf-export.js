const _CONN_TYPE_LABELS = {
    ethernet: 'Ethernet',
    usb:      'USB',
    power:    'Power',
    wifi:     'Wi-Fi',
    zigbee:   'Zigbee',
    zwave:    'Z-Wave',
};

async function _pdfDiagramPage(doc, data) {
    const bgFile = await getUiPreference('diagramBackground').catch(() => null);
    const hasBg  = bgFile && bgFile.path;

    let rawPositions = {};
    try {
        rawPositions = hasBg
            ? (await window.loadMapImagePositions() || {})
            : (await window.loadMapPositions()      || {});
    } catch (_) {}

    const deviceMap     = new Map((data.devices || []).map(d => [String(d.id), d]));
    const positionedIds = Object.keys(rawPositions).filter(id => deviceMap.has(id));

    if (!hasBg && !positionedIds.length) return;

    // ── Pre-load background image once ────────────────────────────────────────
    let bgDataUrl = null;
    let bgImgType = 'JPEG';

    if (hasBg) {
        try {
            const url  = buildAppUrl('api/device-files/content')
                + '?path=' + encodeURIComponent(String(bgFile.path));
            const resp = await fetch(url);
            if (resp.ok) bgDataUrl = await _blobToDataUrl(await resp.blob());
        } catch (e) {
            console.warn('[pdf-export] Background load failed:', e.message);
        }
    }

    if (bgDataUrl) {
        const mimeMatch = bgDataUrl.match(/^data:(image\/[a-zA-Z+]+);/);
        const raw = (mimeMatch ? mimeMatch[1].split('/')[1] : 'jpeg').toUpperCase();
        bgImgType = raw === 'JPG' ? 'JPEG' : raw;
    }

    // ── Compute draw area (aspect-ratio preserving) ───────────────────────────
    const CX = _PDF_MARGIN;
    const CY = _PDF_HEADER_H + 5;
    const CW = _PDF_CW;
    const legendY = _PDF_FOOTER_Y - 14;
    const CH = Math.max(140, legendY - CY - 5);

    let drawX = CX, drawY = CY, drawW = CW, drawH = CH;
    if (bgDataUrl) {
        try {
            const dims  = await _imgNaturalSize(bgDataUrl);
            const ratio = dims.width / dims.height;
            drawH = CW / ratio;
            if (drawH > CH) { drawH = CH; drawW = CH * ratio; }
            drawX = CX + (CW - drawW) / 2;
        } catch (_) {}
    }

    // ── Detect active connection types among positioned (non-wishlist) devices ─
    const validIds = positionedIds.filter(id => {
        const d = deviceMap.get(id);
        return d && d.status !== 'wishlist';
    });
    const posSet = new Set(validIds);

    const activeTypes = new Set();
    (data.devices || []).forEach(device => {
        if (!posSet.has(String(device.id))) return;
        if (Array.isArray(device.ports)) {
            device.ports.forEach(port => {
                if (!port.connectedTo || !posSet.has(String(port.connectedTo))) return;
                const kind = (port.type || '').split('-')[0];
                if (kind) activeTypes.add(kind);
            });
        }
        if (device.wifiAccessPointId   && posSet.has(String(device.wifiAccessPointId)))   activeTypes.add('wifi');
        if (device.zigbeeParentId       && posSet.has(String(device.zigbeeParentId)))       activeTypes.add('zigbee');
        if (device.zwaveControllerId    && posSet.has(String(device.zwaveControllerId)))    activeTypes.add('zwave');
    });

    // Order connection types: wired first, wireless last
    const typeOrder = ['ethernet', 'usb', 'power', 'wifi', 'zigbee', 'zwave'];
    const connTypesToDraw = typeOrder.filter(t => activeTypes.has(t));

    // ── Render one page per diagram ───────────────────────────────────────────
    const _drawDiagramPage = (connType, heading) => {
        doc.addPage();
        _drawPageCanvas(doc);
        _drawPageHeader(doc, heading);

        doc.setFillColor(..._C.cardBg);
        doc.setDrawColor(..._C.cardBorder);
        doc.setLineWidth(0.25);
        doc.roundedRect(CX - 1.5, CY - 1.5, CW + 3, CH + 3, 3, 3, 'FD');

        if (bgDataUrl) {
            doc.addImage(bgDataUrl, bgImgType, drawX, drawY, drawW, drawH);
        }

        if (positionedIds.length) {
            const isOverview = connType === null;
            _renderDiagramOverlay(doc, data, rawPositions, positionedIds, drawX, drawY, drawW, drawH, connType, isOverview, !!bgDataUrl);
            if (isOverview) {
                // Status legend only — no connection lines
                _drawDiagramLegend(doc, legendY, new Set());
            } else {
                _drawDiagramLegend(doc, legendY, new Set([connType]));
            }
        }
    };

    // Always render the overview first (all devices, no connections, no legend)
    _drawDiagramPage(null, 'Diagram — Overview');

    // Then one page per active connection type
    for (const connType of connTypesToDraw) {
        _drawDiagramPage(connType, `Network Diagram — ${_CONN_TYPE_LABELS[connType] || connType}`);
    }
}


// ─── Main orchestrator ─────────────────────────────────────────────────────────

async function generateSmartHomePDF() {
    if (!window.jspdf || typeof window.jspdf.jsPDF !== 'function') {
        showToast('PDF library not loaded. Please refresh and try again.', 'error');
        return;
    }

    const btn    = document.getElementById('export-pdf-btn');
    const status = document.getElementById('export-pdf-status');

    if (btn)    btn.disabled = true;
    if (status) status.style.display = 'inline';

    try {
        const [data, haConfig, settings] = await Promise.all([
            loadData(),
            window.loadHaConfig().catch(() => ({})),
            loadSettings(),
        ]);

        const houseName = ((haConfig && haConfig.location_name) || '').trim() || 'My Smart Home';
        const now       = new Date();
        const dateStr   = now.toLocaleDateString(undefined, {
            year: 'numeric', month: 'long', day: 'numeric',
        });

        // Pre-load the app logo for the cover page
        const logoDataUrl = await _fetchDataUrl(buildAppUrl('img/logo.png')).catch(() => null);

        const config = {
            houseName,
            dateStr,
            settings,
            logoDataUrl,
            stats: {
                totalDevices: data.devices.length,
                totalAreas:   (data.areas  || []).length,
                totalFloors:  (data.floors || []).length,
                working:      data.devices.filter(d => d.status === 'working').length,
                pending:      data.devices.filter(d => d.status === 'pending').length,
                notWorking:   data.devices.filter(d => d.status === 'not-working').length,
            },
        };

        const { jsPDF } = window.jspdf;
        const doc = new jsPDF({ orientation: 'portrait', unit: 'mm', format: 'a4' });

        _pdfCoverPage(doc, config);

        // Reserve page 2 for the Table of Contents (filled in after all pages are rendered)
        doc.addPage();
        const TOC_PAGE = doc.getNumberOfPages(); // = 2
        const nextPg   = () => doc.getNumberOfPages() + 1;

        const tocPages = {};
        tocPages.visualSummary = nextPg();
        _pdfChartsPage(doc, data);

        tocPages.summary = nextPg();
        _pdfSummaryPage(doc, data, config);

        tocPages.deviceDetails = nextPg();
        _pdfDeviceDetailPages(doc, data, config);

        tocPages.diagram = nextPg();
        await _pdfDiagramPage(doc, data);

        tocPages.testCases = nextPg();
        _pdfTestCasesPages(doc, data);

        _pdfFooters(doc);

        // Now fill in the TOC on page 2
        doc.setPage(TOC_PAGE);
        _pdfTocPage(doc, tocPages, config);

        const yy = now.getFullYear();
        const mm = String(now.getMonth() + 1).padStart(2, '0');
        const dd = String(now.getDate()).padStart(2, '0');
        doc.save(`smart-home-inventory-${yy}-${mm}-${dd}.pdf`);

        showToast('PDF exported successfully.', 'success');

    } catch (err) {
        console.error('[pdf-export] Generation failed:', err);
        showToast('PDF export failed: ' + (err.message || String(err)), 'error');
    } finally {
        if (btn)    btn.disabled = false;
        if (status) status.style.display = 'none';
    }
}

// ─── Page 1: Cover ─────────────────────────────────────────────────────────────

function _pdfCoverPage(doc, { houseName, dateStr, stats, logoDataUrl }) {
    const W = _PDF_W;
    const H = _PDF_H;

    doc.setFillColor(..._C.coverBg);
    doc.rect(0, 0, W, H, 'F');

    doc.setFillColor(..._C.coverBgSoft);
    doc.rect(0, 0, W, 116, 'F');

    doc.setFillColor(..._C.coverGlowA);
    doc.circle(W - 6, 28, 42, 'F');
    doc.setFillColor(..._C.coverGlowB);
    doc.circle(18, H - 16, 50, 'F');

    const panelX = 10;
    const panelY = 10;
    const panelW = W - 20;
    const panelH = H - 20;

    doc.setFillColor(..._C.coverPanel);
    doc.roundedRect(panelX, panelY, panelW, panelH, 6, 6, 'F');
    doc.setDrawColor(..._C.coverStroke);
    doc.setLineWidth(0.45);
    doc.roundedRect(panelX, panelY, panelW, panelH, 6, 6, 'D');

    doc.setFillColor(..._C.accent);
    doc.roundedRect(panelX, panelY, panelW, 5, 3, 3, 'F');

    const logoSize = 20;
    const logoX = panelX + 12;
    const logoY = panelY + 11;
    if (logoDataUrl) {
        try {
            doc.addImage(logoDataUrl, 'PNG', logoX, logoY, logoSize, logoSize);
        } catch (_) {
            _drawLogoPlaceholder(doc, logoX + logoSize / 2, logoY + logoSize / 2, logoSize / 2);
        }
    } else {
        _drawLogoPlaceholder(doc, logoX + logoSize / 2, logoY + logoSize / 2, logoSize / 2);
    }

    doc.setFont('helvetica', 'bold');
    doc.setFontSize(16);
    doc.setTextColor(..._C.textLight);
    doc.text('Smart Home Planner', logoX + logoSize + 6, logoY + 8);

    doc.setFont('helvetica', 'normal');
    doc.setFontSize(8.5);
    doc.setTextColor(..._C.textMuted);
    doc.text('planner.smarthomecompared.com', logoX + logoSize + 6, logoY + 12.8);

    doc.setDrawColor(..._C.coverStroke);
    doc.setLineWidth(0.25);
    doc.line(panelX + 10, panelY + 40, panelX + panelW - 10, panelY + 40);

    doc.setFont('helvetica', 'bold');
    doc.setFontSize(31);
    doc.setTextColor(..._C.textLight);
    doc.text('Smart Home', panelX + 12, panelY + 62);
    doc.setTextColor(..._C.accentSoft);
    doc.text('Inventory Report', panelX + 12, panelY + 76);

    const coverHouseName = String(houseName || '').trim();
    const showCoverHouseName = coverHouseName.toLowerCase() !== 'home';
    const dateY = showCoverHouseName ? (panelY + 97) : (panelY + 92);

    if (showCoverHouseName && coverHouseName) {
        doc.setFont('helvetica', 'bold');
        doc.setFontSize(14);
        doc.setTextColor(..._C.textLight);
        doc.text(coverHouseName, panelX + 12, panelY + 90);
    }

    doc.setFont('helvetica', 'normal');
    doc.setFontSize(9.5);
    doc.setTextColor(..._C.textMuted);
    doc.text(dateStr, panelX + 12, dateY);

    if (stats) {
        const boxes = [
            { value: stats.totalDevices, label: 'DEVICES',     sub: `${stats.working} working` },
            { value: stats.totalAreas,   label: 'AREAS',       sub: ' '                        },
            { value: stats.totalFloors,  label: 'FLOORS',      sub: ' '                        },
        ];

        const boxW   = 49;
        const boxH   = 34;
        const gapX   = 5;
        const total  = boxes.length * boxW + (boxes.length - 1) * gapX;
        const startX = panelX + (panelW - total) / 2;
        const boxY   = panelY + 112;

        boxes.forEach((box, i) => {
            const bx = startX + i * (boxW + gapX);

            doc.setFillColor(..._C.coverCard);
            doc.roundedRect(bx, boxY, boxW, boxH, 4, 4, 'F');

            doc.setFillColor(..._C.accent);
            doc.roundedRect(bx, boxY, boxW, 3, 1.5, 1.5, 'F');

            doc.setDrawColor(..._C.coverStroke);
            doc.setLineWidth(0.2);
            doc.roundedRect(bx, boxY, boxW, boxH, 4, 4, 'D');

            doc.setFont('helvetica', 'bold');
            doc.setFontSize(23);
            doc.setTextColor(..._C.textLight);
            doc.text(String(box.value), bx + boxW / 2, boxY + 18.5, { align: 'center' });

            doc.setFont('helvetica', 'bold');
            doc.setFontSize(7);
            doc.setTextColor(..._C.textMuted);
            doc.text(box.label, bx + boxW / 2, boxY + 27, { align: 'center' });

            if (box.sub && box.sub.trim()) {
                doc.setFont('helvetica', 'normal');
                doc.setFontSize(6.5);
                doc.setTextColor(..._C.textMuted);
                doc.text(box.sub, bx + boxW / 2, boxY + 31.5, { align: 'center' });
            }
        });
    }

    if (stats) {
        const pills = [
            { label: 'Working',     count: stats.working,    color: _C.working },
            { label: 'Pending',     count: stats.pending,    color: _C.pending },
            { label: 'Not Working', count: stats.notWorking, color: _C.notWorking },
        ];

        const pillH   = 9;
        const pillGap = 5;
        const contentW = panelW - 24;
        const pillW   = (contentW - pillGap * (pills.length - 1)) / pills.length;
        const pillX0  = panelX + 12;
        const pillY   = panelY + 152;

        pills.forEach((pill, i) => {
            const px = pillX0 + i * (pillW + pillGap);

            doc.setFillColor(...pill.color);
            doc.roundedRect(px, pillY, pillW, pillH, 2.2, 2.2, 'F');

            doc.setFont('helvetica', 'bold');
            doc.setFontSize(7);
            doc.setTextColor(255, 255, 255);
            doc.text(`${pill.label}: ${pill.count}`, px + pillW / 2, pillY + pillH / 2, {
                align: 'center', baseline: 'middle',
            });
        });
    }

}

/** Draws a simple geometric placeholder when the logo image is unavailable. */
function _drawLogoPlaceholder(doc, cx, cy, r) {
    doc.setFillColor(..._C.accent);
    doc.circle(cx, cy, r, 'F');
    doc.setFont('helvetica', 'bold');
    doc.setFontSize(r * 1.8);
    doc.setTextColor(255, 255, 255);
    doc.text('SH', cx, cy + r * 0.35, { align: 'center' });
}

// ─── Summary pages (full dashboard stats) ─────────────────────────────────────

function _pdfSummaryPage(doc, data, { settings }) {
    doc.addPage();

    const { devices, areas, floors, labels } = data;
    const areaMap  = new Map((areas  || []).map(a => [a.id,  a]));
    const labelMap = new Map((labels || []).map(l => [l.id,  l]));

    // ── Shared autoTable base options ─────────────────────────────────────────
    const base = {
        theme:              'grid',
        styles:             {
            fontSize: 8.3,
            cellPadding: 2.1,
            overflow: 'linebreak',
            lineColor: _C.tableGrid,
            lineWidth: 0.12,
            textColor: _C.textOnLight,
        },
        headStyles:         {
            fillColor: _C.tableHead,
            textColor: _C.textLight,
            fontStyle: 'bold',
            fontSize: 8.2,
            lineColor: _C.tableHead,
            lineWidth: 0.12,
        },
        alternateRowStyles: { fillColor: _C.rowAlt },
        margin:             { top: _PDF_HEADER_H + 1, left: _PDF_MARGIN, right: _PDF_MARGIN, bottom: _PDF_BOTTOM_MARGIN },
        didDrawPage:        () => _drawPageHeader(doc, 'Summary'),
    };

    /**
     * Renders a titled breakdown table with Count and % columns.
     * Uses a colSpan heading row as the visual section title.
     * @param {string}   sectionTitle – appears in a full-width accent header row
     * @param {string[]} colHeaders   – e.g. ['Type', 'Devices', '%']
     * @param {Array[]}  rows         – raw rows [[label, count], …]
     * @param {number}   total        – denominator for % calculation
     * @param {number}   startY
     * @param {number}   [barCol]     – column index to render as a mini progress bar
     */
    const sectionTable = (sectionTitle, colHeaders, rows, total, startY, barCol) => {
        const ncols   = colHeaders.length;
        const pctRows = rows.map(([label, count, extra]) => {
            const pct = total > 0 ? Math.round((count / total) * 100) : 0;
            return ncols === 3 && extra === undefined
                ? [label, count, pct + '%']
                : [label, count, extra !== undefined ? extra : pct + '%'];
        });

        const opts = {
            ...base,
            startY,
            head: [
                [{ content: sectionTitle, colSpan: ncols, styles: {
                    fillColor: _C.accent, textColor: [255, 255, 255],
                    fontStyle: 'bold', fontSize: 9, halign: 'left',
                } }],
                colHeaders,
            ],
            body: pctRows,
            columnStyles: {
                0: { cellWidth: 'auto' },
                1: { cellWidth: 28, halign: 'right', fontStyle: 'bold' },
                ...(ncols >= 3 ? { 2: { cellWidth: 22, halign: 'right' } } : {}),
            },
        };

        // Optional mini-bar rendering in a dedicated last column
        if (barCol !== undefined) {
            opts.didDrawCell = function (d) {
                if (d.section !== 'body' || d.column.index !== barCol) return;
                const pct   = parseInt(d.row.raw[d.row.raw.length - 1]) || 0;
                const barW  = d.cell.width - 4;
                const barH  = 2.5;
                const barX  = d.cell.x + 2;
                const barY  = d.cell.y + (d.cell.height - barH) / 2;
                doc.setFillColor(..._C.barBg);
                doc.rect(barX, barY, barW, barH, 'F');
                doc.setFillColor(..._C.barFill);
                doc.rect(barX, barY, Math.max(0, (pct / 100) * barW), barH, 'F');
            };
        }

        doc.autoTable(opts);
    };

    let y = _PDF_HEADER_H + 1;

    // ══ Section 1: Device Status ══════════════════════════════════════════════
    const totalAll  = devices.length;
    const active    = devices.filter(d => d.status !== 'wishlist');

    doc.autoTable({
        ...base,
        startY: y,
        head: [
            [{ content: 'Device Overview', colSpan: 2, styles: {
                fillColor: _C.accent, textColor: [255, 255, 255], fontStyle: 'bold', fontSize: 9,
            } }],
            ['Metric', 'Value'],
        ],
        body: [
            ['Total Devices',           totalAll],
            ['Active (non-wishlist)',    active.length],
            ['Areas',                   (areas  || []).length],
            ['Floors',                  (floors || []).length],
        ],
        columnStyles: {
            0: { cellWidth: 'auto' },
            1: { cellWidth: 28, halign: 'right', fontStyle: 'bold' },
        },
    });
    y = doc.lastAutoTable.finalY + 5;

    // ══ Section 2: Brand ══════════════════════════════════════════════════════
    const byBrand  = _groupBy(devices.filter(d => d.brand), d => d.brand);
    const brandTotal = devices.filter(d => d.brand).length;
    const brandRows  = Object.entries(byBrand)
        .sort((a, b) => b[1].length - a[1].length)
        .map(([k, v]) => {
            const lbl = (settings && settings.brands)
                ? (getFriendlyOption(settings.brands, k, formatDeviceType) || k) : k;
            return [lbl, v.length];
        });

    if (brandRows.length) {
        sectionTable('Devices by Brand', ['Brand', 'Devices', '%'], brandRows, brandTotal, y);
        y = doc.lastAutoTable.finalY + 5;
    }

    // ══ Section 5: Device Type ════════════════════════════════════════════════
    const byType   = _groupBy(devices.filter(d => d.type), d => d.type);
    const typeTotal = devices.filter(d => d.type).length;
    const typeRows  = Object.entries(byType)
        .sort((a, b) => b[1].length - a[1].length)
        .map(([k, v]) => {
            const lbl = (settings && settings.types)
                ? (getFriendlyOption(settings.types, k, formatDeviceType) || k)
                : _fmtType(k);
            return [lbl, v.length];
        });

    if (typeRows.length) {
        sectionTable('Devices by Type', ['Type', 'Devices', '%'], typeRows, typeTotal, y);
        y = doc.lastAutoTable.finalY + 5;
    }

    // ══ Section 6: Installed / Controlled Areas ═══════════════════════════════
    const _resolveAreaName = (areaId, fallbackLabel) => {
        const normalized = String(areaId || '').trim();
        if (!normalized) return fallbackLabel;
        const area = areaMap.get(normalized) || areaMap.get(areaId);
        return area ? area.name : normalized;
    };
    const _areaRows = (selector, emptyLabel) => {
        const counts = {};
        devices.forEach((device) => {
            const areaId = String(selector(device) || '').trim();
            const key = areaId || '__none__';
            counts[key] = (counts[key] || 0) + 1;
        });
        return Object.entries(counts)
            .sort((a, b) => b[1] - a[1])
            .map(([areaId, count]) => {
                if (areaId === '__none__') return [emptyLabel, count];
                return [_resolveAreaName(areaId, emptyLabel), count];
            });
    };

    const controlledAreaRows = _areaRows((device) => device.controlledArea, 'No Controlled Area');
    if (controlledAreaRows.length) {
        sectionTable('Devices by Controlled Area', ['Controlled Area', 'Devices', '%'], controlledAreaRows, totalAll, y);
        y = doc.lastAutoTable.finalY + 5;
    }

    const installedAreaRows = _areaRows((device) => device.area, 'No Installed Area');
    if (installedAreaRows.length) {
        sectionTable('Devices by Installed Area', ['Installed Area', 'Devices', '%'], installedAreaRows, totalAll, y);
        y = doc.lastAutoTable.finalY + 5;
    }

    // ══ Section 8: Battery ════════════════════════════════════════════════════
    const battDevices = devices.filter(d => d.power === 'battery' && d.batteryType);
    const byBattType  = _groupBy(battDevices, d => d.batteryType);
    const battRows    = Object.entries(byBattType)
        .sort((a, b) => b[1].length - a[1].length)
        .map(([type, devs]) => {
            const cells = devs.reduce((s, d) => s + (Number(d.batteryCount) || 1), 0);
            return [type, devs.length, cells, '-'];
        });

    if (battRows.length) {
        doc.autoTable({
            ...base,
            startY: y,
            head: [
                [{ content: 'Total Batteries by Type', colSpan: 4, styles: {
                    fillColor: _C.accent, textColor: [255, 255, 255], fontStyle: 'bold', fontSize: 9,
                } }],
                ['Battery Type', 'Devices', 'Total Cells', '% of Devices'],
            ],
            body: battRows.map(([t, d, c]) => [
                t, d, c,
                battDevices.length > 0 ? Math.round((d / battDevices.length) * 100) + '%' : '-',
            ]),
            columnStyles: {
                0: { cellWidth: 'auto' },
                1: { cellWidth: 25, halign: 'right', fontStyle: 'bold' },
                2: { cellWidth: 28, halign: 'right' },
                3: { cellWidth: 28, halign: 'right' },
            },
        });
        y = doc.lastAutoTable.finalY + 5;
    }

    // ══ Section 9: Labels ═════════════════════════════════════════════════════
    const labelCount = {};
    devices.forEach(d => {
        (d.labels || []).forEach(lid => {
            const lo   = labelMap.get(String(lid || '').trim());
            const name = lo ? lo.name : (String(lid || '') || 'Unknown');
            labelCount[name] = (labelCount[name] || 0) + 1;
        });
    });
    const labelRows = Object.entries(labelCount).sort((a, b) => b[1] - a[1]).map(([n, c]) => [n, c]);
    if (labelRows.length) {
        sectionTable('Devices by Label', ['Label', 'Devices', '% of Total'], labelRows, totalAll, y);
        y = doc.lastAutoTable.finalY + 5;
    }

    // ══ Section 10: Device alert lists ════════════════════════════════════════
    const _deviceList = (title, filtered, titleColor, includeAreaBreakdown = false) => {
        if (!filtered.length) return;
        const listRows = filtered.map((d) => {
            if (includeAreaBreakdown) {
                return [
                    d.name || 'Unnamed',
                    d.brand || '-',
                    _resolveAreaName(d.controlledArea, '-'),
                    _resolveAreaName(d.area, '-')
                ];
            }
            const area = areaMap.get(d.controlledArea || d.area || '');
            return [d.name || 'Unnamed', d.brand || '-', area ? area.name : '-'];
        });
        const headers = includeAreaBreakdown
            ? ['Device Name', 'Brand', 'Controlled Area', 'Installed Area']
            : ['Device Name', 'Brand', 'Area'];
        const colStyles = includeAreaBreakdown
            ? { 0: { cellWidth: 'auto' }, 1: { cellWidth: 30 }, 2: { cellWidth: 36 }, 3: { cellWidth: 36 } }
            : { 0: { cellWidth: 'auto' }, 1: { cellWidth: 38 }, 2: { cellWidth: 40 } };
        doc.autoTable({
            ...base,
            startY: doc.lastAutoTable.finalY + 5,
            head: [
                [{ content: `${title} (${filtered.length})`, colSpan: headers.length, styles: {
                    fillColor: titleColor, textColor: [255, 255, 255], fontStyle: 'bold', fontSize: 9,
                } }],
                headers,
            ],
            body: listRows,
            columnStyles: colStyles,
        });
    };

    const pending    = devices.filter(d => d.status === 'pending');
    const notWorking = devices.filter(d => d.status === 'not-working');

    _deviceList('Pending Devices',     pending,     _C.pending, true);
    _deviceList('Not Working Devices', notWorking,  _C.notWorking);
}

